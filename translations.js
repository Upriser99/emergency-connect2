// Translation helper function
function t(key, lang = null) {
    const currentLang = lang || App.currentLang || 'en';
    return TRANSLATIONS[currentLang][key] || TRANSLATIONS.en[key] || key;
}

// Update all UI text based on current language
function updateAllText() {
    const lang = App.currentLang;

    // Header
    document.querySelector('.app-title').textContent = `🚨 ${t('appTitle')}`;

    // Home page
    const sectionTitle = document.querySelector('#homeView .section-title');
    if (sectionTitle) {
        sectionTitle.textContent = t('selectService');
    }

    // Category names
    const categories = {
        'police': 'police',
        'hospital': 'hospitals',
        'fire': 'fire',
        'petrol': 'petrol',
        'ambulance': 'ambulance',
        'pharmacy': 'pharmacy'
    };

    Object.keys(categories).forEach(cat => {
        const card = document.querySelector(`.category-card.${cat} h3`);
        if (card) {
            card.textContent = t(categories[cat]);
        }
    });

    // Coming soon badges
    document.querySelectorAll('.coming-soon-badge').forEach(badge => {
        badge.textContent = t('comingSoon');
    });

    // Emergency numbers title
    const emergencyTitle = document.querySelector('.emergency-numbers h3');
    if (emergencyTitle) {
        emergencyTitle.textContent = t('emergencyNumbers');
    }

    // Service view
    const serviceTitle = document.getElementById('serviceTitle');
    if (serviceTitle && serviceTitle.textContent.includes('Services Near You')) {
        serviceTitle.textContent = t('servicesNearYou');
    }

    // Buttons
    const backBtn = document.querySelector('.back-btn');
    if (backBtn) {
        backBtn.textContent = `← ${t('back')}`;
    }

    const listBtn = document.getElementById('listViewBtn');
    if (listBtn) {
        listBtn.innerHTML = `📋 ${t('list')}`;
    }

    const mapBtn = document.getElementById('mapViewBtn');
    if (mapBtn) {
        mapBtn.innerHTML = `🗺️ ${t('map')}`;
    }

    // Search placeholder
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.placeholder = t('searchPlaceholder');
    }

    // Loading indicator
    const loadingText = document.querySelector('#loadingIndicator p');
    if (loadingText) {
        loadingText.textContent = t('findingServices');
    }

    // SOS button
    const sosBtn = document.querySelector('.sos-button');
    if (sosBtn) {
        sosBtn.title = t('emergencyCall');
    }

    // Phase 3: Favorites & Contacts
    const favoritesTitle = document.querySelector('.favorites-section h3');
    if (favoritesTitle) {
        favoritesTitle.textContent = `⭐ ${t('favorites')}`;
    }

    const contactsTitle = document.querySelector('.contacts-section h3');
    if (contactsTitle) {
        contactsTitle.textContent = `👥 ${t('quickContacts')}`;
    }

    // Phase 4: Quick Action Bar
    const actionLabels = {
        'sirenBtn': 'siren',
        'sosBtn': 'sos'
    };

    Object.keys(actionLabels).forEach(id => {
        const btn = document.getElementById(id);
        if (btn) {
            const label = btn.querySelector('.action-label');
            if (label) {
                label.textContent = t(actionLabels[id]);
            }
        }
    });

    // Phase 5: Emergency Guides
    const guidesTitle = document.querySelector('.guides-section h3');
    if (guidesTitle) {
        guidesTitle.textContent = `📚 ${t('emergencyGuides')}`;
    }

    const healthCardTitle = document.querySelector('.health-card-section h3');
    if (healthCardTitle) {
        healthCardTitle.textContent = `💳 ${t('healthCard')}`;
    }

    // Update service counts
    App.updateServiceCounts();
}
